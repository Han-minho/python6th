a = 4
b = 2

total1 = a+b
total2 = a-b
total3 = a*b
total4 = a/b
total5 = a%b
total6 = a**b
total7 = a//b
total8 = -a//b

#덧셈
print("total a+b : ",total1,type(total1))
# 뺄셈
print("total a-b : ",total2,type(total2))
# 곱셈
print("total a*b : ",total3,type(total3))
# 나눗셈
print("total a/b : ",total4,type(total4))
# 나머지
print("total a%b : ",total5,type(total5))
# 거듭 제곱
print("total a**b : ",total6,type(total6))
# 양수 몫
print("total a//b : ",total7,type(total7))
# 음수 몫
print("total -a//b : ",total8,type(total8))
